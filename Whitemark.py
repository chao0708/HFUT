whitespaces = [0x00A0, 0x2000, 0x2001, 0x2002, 0x2003, 0x2004, 0x2005, 0x2006,
               0x2007, 0x2008, 0x2009, 0x200A, 0x202F, 0x205F, 0x200B, 0x2060]


# 将所有的编码替换 这里的0x2004可以随意选择
def add_watermark(t):
    return t.replace(chr(0x0020), chr(0x2004))


def detect_watermark(s):
    return chr(0x2004) in s
